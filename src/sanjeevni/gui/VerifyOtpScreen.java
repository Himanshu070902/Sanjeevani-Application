private boolean isValidOtp(){
    boolean result=false;
    try{
        int otp=Integer.parseInt(txtOTP.getText().trim());
        if(otp==patient.getOtp()){
           result=true;
        }  
    }
    catch(NumberFormatException e){
        JOptionPane.showMessageDialog(null,"Please Enter numeric values!");  
    }
    finally{
    return result;
    }
}
    private boolean checkPhoneNo(String No){
        if(No.length()!=10)
            return false;
        for(int i=0;i<No.length();i++){
            if(No.charAt(i)<47||No.charAt(i)>58)
                return false;
        }
        return true;
    } 
