    private void lblVerifyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblVerifyMouseClicked
     if(isValidOtp()==false){
       JOptionPane.showMessageDialog(null,"Invalid OTP ,Try Again! ","Error",JOptionPane.ERROR_MESSAGE);  
       return;
     }
     try
     {
         AppointmentPojo appojo=new AppointmentPojo();
         appojo.setDateTime(patient.getpDate().toString());
         appojo.setMobileNo(patient.getMobileNo());
         appojo.setPatientId(patient.getPatientId());
         appojo.setPatientName(txtPatName.getText().trim());
         appojo.setStatus("REQUEST");
         appojo.setOpd(patient.getOpd().trim());
         appojo.setDoctorName(DoctorDao.getDoctorNameById(patient.getDoctorId()));
         boolean result1= PatientDao.addPatients(patient);
         boolean result2=AppointmentDao.addAppointment(appojo);
         if(result1==result2){
             JOptionPane.showMessageDialog(null,"Patient record saved!");   
             ViewAllPatientFrame vpf=new ViewAllPatientFrame();
             vpf.setVisible(true);
             this.dispose();
         }
         else{
             JOptionPane.showMessageDialog(null, "Sorry! could not add the patient record");
             return;
         }
     }
     catch(SQLException e){
       JOptionPane.showMessageDialog(null, "Error in DB!");
       e.printStackTrace();
       
     }
     
    }//GEN-LAST:event_lblVerifyMouseClicked

