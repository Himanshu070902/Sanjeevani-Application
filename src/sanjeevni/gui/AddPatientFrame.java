    private boolean isContactValid(){
        contactNo=txtContactNo.getText();
        char []mob=contactNo.toCharArray();
        for(char ch:mob){
            if(Character.isDigit(ch)==false){
                return false;
            }
        }
        if(contactNo.length()==10){
            return true;
        }
        return false;
    }
    private boolean validateInput(){
        pat=new PatientPojo();
        pat.setPatientId(txtPatId.getText().trim());
        pat.setCity(txtCity.getText());
        pat.setFirstName(txtFirstName.getText());
        pat.setLastName(txtLastName.getText());
        pat.setpDate(dt);
        pat.setGender(jcGender.getSelectedItem().toString());
        pat.setAddress(txtAddress.getText().trim());
        pat.setMobileNo(txtContactNo.getText().trim());
        pat.setmStatus(jcMStatus.getSelectedItem().toString());
        pat.setDoctorId(jcDocId.getSelectedItem().toString());
       if(txtFirstName.getText().isEmpty()||txtLastName.getText().isEmpty()||txtContactNo.getText().isEmpty()||txtCity.getText().isEmpty()||txtAddress.getText().isEmpty()){
           JOptionPane.showMessageDialog(null,"Please fill the details!");
           return false;  
        }
        try{
              pat.setAge(Integer.parseInt(txtAge.getText().trim()));
       }
       catch(NumberFormatException e){
           JOptionPane.showMessageDialog(null,"Invalid Age"+e);
          return false;
       }
       
        if(isContactValid()==false){
            JOptionPane.showMessageDialog(null,"Invalid PhoneNo.");
            return false;
        }
        return true;  
    }
