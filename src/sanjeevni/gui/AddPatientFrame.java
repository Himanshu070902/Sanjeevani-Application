   private void generatePatId() {
       try{
           String patId=PatientDao.generateId();
           txtPatId.setText(patId);
           Date date =new Date();
           SimpleDateFormat sdf=new SimpleDateFormat("dd-MMM-yyyy");
           String dateStr=sdf.format(date);
           txtDate.setText(dateStr);
           java.util.Date d1=sdf.parse(dateStr);
           long ms=d1.getTime();
           dt=new java.sql.Date(ms);
       }
        catch(SQLException e){
           JOptionPane.showMessageDialog(null,"Error in DB!"+e.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);
           e.printStackTrace();
       }
       catch(ParseException e){
                JOptionPane.showMessageDialog(null,"Error in Date conversion!"+e.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);
           e.printStackTrace();
       }
       
    }

    private void loadDocId() {
       try{
           docIdList=DoctorDao.getAllDoctorId();
           for(String id:docIdList){
               jcDocId.addItem(id);
           } 
       }
       catch(SQLException e){
           JOptionPane.showMessageDialog(null,"Error in DB!"+e.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);
           e.printStackTrace();
       }
    }
