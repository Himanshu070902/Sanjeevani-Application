    private void btnRegisterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegisterMouseClicked
    boolean validateInput=isValidInput();
      if(validateInput==false){
          JOptionPane.showMessageDialog(null,"Please input all the details!","ERROR",JOptionPane.ERROR_MESSAGE );
          return;
      }
       String passwordValid=matchPassword();
       if(passwordValid!=null){
           JOptionPane.showMessageDialog(null, passwordValid);
           return;
       }
     try{
         ReceptionistPojo recep=new ReceptionistPojo();
         recep.setGender(jcGender.getSelectedItem().toString());
         recep.setRecepId(txtReceptionistId.getText());
         recep.setRecepName(recepName);
         UserPojo user=new UserPojo();
         user.setUserId(loginId);
         user.setUserName(recepName);
         user.setUserType("RECEPTIONIST");
         String encPassword=PasswordEncryption.getEncryptedPassword(pwd);
         user.setPassword(encPassword);
         boolean result1=ReceptionistDao.addRecep(recep);
         boolean result2=UserDao.addUser(user);
           if(result1==true&&result2==true){
               JOptionPane.showMessageDialog(null,"Receptionist successfully registered!");
                clearAll();
                jcEmpId.removeItem(recepId);
               return;
           }
           JOptionPane.showMessageDialog(null,"Registration unsuccessfully!");
           clearAll();
           return;
       }
     catch(SQLException e){
         JOptionPane.showMessageDialog(null,"Error in DB!","ErrorInRegisterRecep",JOptionPane.ERROR_MESSAGE );
     }
     
    }//GEN-LAST:event_btnRegisterMouseClicked

