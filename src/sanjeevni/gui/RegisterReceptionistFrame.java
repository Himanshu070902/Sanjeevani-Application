    private void loadRecepDetails() {
       try{
           unRegRecepList =EmpDao.getAllUnRegisteredRecep();
           Set<String> RecepId=unRegRecepList.keySet();
           if(RecepId==null){
               JOptionPane.showMessageDialog(null, "No Receptionist Present!");
               return;
           }
           for(String s:RecepId){
               jcEmpId.addItem(s);
           }
           
       }
         catch(SQLException e){
        JOptionPane.showMessageDialog(null,"Error in DB "+e.getMessage(),"ERRORinRegisterRecep",JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
       }
    }
    
